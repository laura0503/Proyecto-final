class Horario {
  final int id_horario;
  final String texto;
  final String horario_inicio;
  final String horario_fin;
  final bool recreo;

  Horario({
    required this.id_horario,
    required this.texto,
    required this.horario_inicio,
    required this.horario_fin,
    required this.recreo,
  });
}
